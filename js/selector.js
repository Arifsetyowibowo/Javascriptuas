const hari = 3;
let hasil;

switch (hari) {
    case 1:
        hasil = 'Minggu';
        break;
    case 2:
        hasil = 'Senin';
        break;

    case 3:
        hasil = 'Selasa';
        break;


    case 4:
        hasil = 'Rabu';
        break;


    default:
        hasil = "belum dibuat";
        break;

}
console.log(hasil);

